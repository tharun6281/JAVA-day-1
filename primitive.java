public class primitive
{
    public static void main(String[] args)
    {
        byte b=120;
        short a=150;
        int c=10000;
        long l=123456;
        float f=12.12345f;
        double d=150.9876d;
        System.out.println(b);
        System.out.println(a);
        System.out.println(c);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
    }
}
